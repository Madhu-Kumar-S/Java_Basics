package control_statements;

public class Switch_Statement {
    public static void main(String[] args)
    {
        char color = 'b';
        switch(color)
        {
            case 'r':
                System.out.println("red");
                break;
            case 'g':
                System.out.println("green");
                break;
            case 'b':
                System.out.println("blue");
                break;
            case 'y':
                System.out.println("yellow");
                break;
        }
    }
}
