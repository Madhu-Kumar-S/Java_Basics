package basics;

public class Data_Types {
    public static void main(String[] args)
    {
        byte b = 123;  // 1 byte
        short s = 2234;  // 2 bytes
        int i = 33456778;  // 4 bytes
        long l = 123456789;  // 8  bytes
        float f = 3.145F;  // 4 bytes
        double d = 2.456789e8;  // 8 bytes, here e8 represents 10^8
        char c = 'X';  // 2 bytes
        String str = "Welcome";
        boolean response = true;  // 1 bit
        boolean result = false;
        System.out.println(b+"\n"+s+"\n"+i+"\n"+l+"\n"+f+"\n"+d+"\n"+c+"\n"+str+"\n"+response+"\n"+result);

    }
}
