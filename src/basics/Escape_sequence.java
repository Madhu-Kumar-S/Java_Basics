package basics;

public class Escape_sequence {
    public static void main(String[] args)
    {
        System.out.println("Hi, welcome to java programming.");
        System.out.println("Hi,\n welcome to java programming.");
        System.out.println("Hi,\t welcome to java programming.");
        System.out.println("Hi,\r welcome to java programming.");
        System.out.println("Hi,\b welcome to java programming.");
        System.out.println("Hi,\\ welcome to java programming.");
        System.out.println("Hi,\" welcome to java programming.");
        System.out.println("Hi,\' welcome to java programming.");
    }
}
